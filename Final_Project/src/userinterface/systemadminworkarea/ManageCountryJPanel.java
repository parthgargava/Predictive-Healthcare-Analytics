    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed
        // TODO add your handling code here:
        String name=nameJTextField.getText();
        Validation v = new Validation();
        
           String blank = "";
        if (name.equals(blank)) {
            JOptionPane.showMessageDialog(null, "Any field can not be empty");
        } 
        
        else if(!v.alphaNumericCheck(name))
        {
             JOptionPane.showMessageDialog(null, "Network Name can only be alphabets and numbers");
             nameJTextField.setBackground(Color.yellow);
        }
        
        else{
        ecoSystem.createCountry(name);
        populateTable();
        JOptionPane.showMessageDialog(this, "WORLD ADDED SUCCESSFULLY", "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
        nameJTextField.setText("");
        }
    }//GEN-LAST:event_submitJButtonActionPerformed


