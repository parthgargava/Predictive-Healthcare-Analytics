    public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) countryJTable.getModel();
        dtm.setRowCount(0);

        Object[] row = new Object[2];
        for (Country country : ecoSystem.getCountryList()) {
            row[0] = country.getCountryID();
            row[1] = country;
            dtm.addRow(row);
        }
    }

